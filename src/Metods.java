public class Metods {
    boolean isEven(int całkowita) {
        return całkowita % 2 == 0;
    }
    boolean isOdd(int całkowita) {
        return  całkowita % 3 == 0;
    }
    double circleField(double Pi, double r) {
        return  Pi * (r * r);
    }
    double power(int całkowita, int kwadrat) {
        return Math.pow(całkowita,kwadrat);
    }
}
